    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        InputPenjualan z = new InputPenjualan();
        PenjualanData x = new PenjualanData();

        x.nama_produk = txtProduk.getText();        
        x.id_produk = Integer.parseInt(txtID.getText());
        
        try {
            sql = "SELECT harga AS harga_satuan FROM tb_produk WHERE id_produk='" + txtID.getText() + "'";
            res = stat.executeQuery(sql);
            if (res.next()) {
                x.harga_satuan = Integer.valueOf(res.getString("harga_satuan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        
        z.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnPilihActionPerformed

