    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        if (txtCari.getText().isEmpty()) {
            load_table();
        } else {
            DefaultTableModel table = new DefaultTableModel();
    table.addColumn("ID");
    table.addColumn("Nama Produk");
    table.addColumn("Harga");
    table.addColumn("Stok");
    table.addColumn("Deskripsi");   
    table.addColumn("Kategori");
            try {
                sql = "SELECT * FROM tb_produk WHERE nama_produk LIKE '%" + txtCari.getText() + "%' "
                        + "ORDER BY nama_produk";
                res = stat.executeQuery(sql);
                while (res.next()) {
                    table.addRow(new Object[]{
                res.getString("id_produk"),
                res.getString("nama_produk"),
                res.getInt("harga"),
                res.getInt("stok"),
                res.getString("deskripsi"),
                res.getString("nama_kategori"),});
                }
                tabelProduk.setModel(table);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
        }
    }//GEN-LAST:event_txtCariKeyPressed

